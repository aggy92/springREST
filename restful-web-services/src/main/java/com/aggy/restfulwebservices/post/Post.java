package com.aggy.restfulwebservices.post;

import com.aggy.restfulwebservices.user.User;

public class Post {
    private Long id;

    private String text;

    private User author;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public User getAuthor() {
        return author;
    }

    public void setAuthor(User author) {
        this.author = author;
    }
}
